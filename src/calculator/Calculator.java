package calculator;

public class Calculator {
    void add(double a, double b) {
        System.out.println(a + b);
    }

    void substract(double a, double b) {
        System.out.println(a - b);
    }

    void multiply(double a, double b) {
        System.out.println(a * b);
    }

    void divide(double a, double b) {
        System.out.println(a / b);
    }
}
